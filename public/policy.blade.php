	<h2 id="header2">Policy brief & purpose</h2>
	<p id="p1"> We designed our <b>company confidentiality policy</b> to explain how we expect <br> our employees to treat confidential information. Employees will unavoidably receive <br> and handle personal and private information about clients, partners <br> and our company. We want to make sure that this information is well-protected. </p>
	<p id="p2"> We must protect this information for two reasons. It may: </p>
	<ul id="l1">
		<li> Be legally binding (e.g. sensitive customer data.) </li>
		<li> Constitute the backbone of our business, giving us a competitive advantage (e.g. <br> business processes.) </li>
	</ul>
</div>
<div class="c2">
	<h3 id="header3">Scope</h3>
	<p id="p3"> This policy affects all employees, including board members, investors, contractors <br> and volunteers, who may have access to confidential information. </p>
</div>
<div class="c3">
	<h4 id="header4">What employees should do:</h4>
	<ul id="l2">
		<li> Lock or secure confidential information at all times </li>
		<li> Shred confidential documents when they’re no longer needed </li>
		<li> Make sure they only view confidential information on secure devices </li>
		<li> Only disclose information to other employees when it’s necessary and authorized </li>
		<li> Keep confidential documents inside our company’s premises unless it’s absolutely necessary to move them </li>
	</ul>
</div>
<div class="c4">
	<h5 id="header5">What employees shouldn’t do:</h5>
	<ul id="l3">
		<li> Use confidential information for any personal benefit or profit </li>
		<li> Disclose confidential information to anyone outside of our company </li>
		<li> Replicate confidential documents and files and store them on insecure devices </li>
	</ul>
	<p id="p4"> When employees stop working for our company, they’re obliged to return any <br> confidential files and delete them from their personal devices. </p>
</div>
